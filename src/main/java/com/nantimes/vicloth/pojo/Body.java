package com.nantimes.vicloth.pojo;

public class Body {
	private int id;
	private String height;
	private String weight;
	private String armLength;
	private String legLength;
	private String waist ;
	private String bust;
    private String eyeMidColor;
    private String face;
    private String hairId;
    private String gender;
    
    public Body() {
  	
    }
    
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getArmLength() {
		return armLength;
	}
	public void setArmLength(String armLength) {
		this.armLength = armLength;
	}
	public String getLegLength() {
		return legLength;
	}
	public void setLegLength(String legLength) {
		this.legLength = legLength;
	}
	public String getWaist() {
		return waist;
	}
	public void setWaist(String waist) {
		this.waist = waist;
	}
	public String getBust() {
		return bust;
	}
	public void setBust(String bust) {
		this.bust = bust;
	}
	public String getEyeMidColor() {
		return eyeMidColor;
	}
	public void setEyeMidColor(String eyeMidColor) {
		this.eyeMidColor = eyeMidColor;
	}
	public String getFace() {
		return face;
	}
	public void setFace(String face) {
		this.face = face;
	}
	public String getHairId() {
		return hairId;
	}
	public void setHairId(String hairId) {
		this.hairId = hairId;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}
    
    
    

}
